#!/usr/bin/env python3
"""
generate-audit-report.py

Sprint 11 Criterion 12 implementation. Walks every Sprint 6–10 markdown eval,
extracts the trailing `## Transcript Trailer` JSON block where present, and
writes `tests/audit-report.md` summarizing:

  (a) verified_via_command=true entries with a matching tool_calls task_id
      (consistent — the verifier did run)
  (b) verified_via_command=true entries with NO matching tool_calls task_id
      (the calibration-drift case Sprint 10 was designed to catch)
  (c) verified_via_command=false entries (LLM-judge / prose-graded — expected)

Sprint 9 introduced the trailer protocol but predates the per-criterion
`criteria_audit` array (Sprint 10). Evals without `criteria_audit` are reported
as "no per-criterion audit channel" rather than as failures.

Exit 0 unless an eval's trailer is malformed.
"""

from __future__ import annotations

import json
import re
import sys
from pathlib import Path
from typing import Any

TRAILER_HEADING_RE = re.compile(r"^##\s+Transcript Trailer.*$", re.MULTILINE)
JSON_FENCE_RE = re.compile(r"```json\s*\n(.*?)```", re.DOTALL)


def find_trailer(text: str) -> dict[str, Any] | None:
    headings = list(TRAILER_HEADING_RE.finditer(text))
    if not headings:
        return None
    after_heading = text[headings[-1].end():]
    fence = JSON_FENCE_RE.search(after_heading)
    if fence is None:
        return None
    try:
        return json.loads(fence.group(1))
    except json.JSONDecodeError:
        return None


def categorize_audit(trailer: dict[str, Any]) -> dict[str, list[str]]:
    audit_entries = trailer.get("criteria_audit", [])
    tool_calls = trailer.get("tool_calls", [])
    tool_call_task_ids: set[str] = {
        tc["task_id"] for tc in tool_calls
        if isinstance(tc, dict) and "task_id" in tc
    }
    consistent: list[str] = []
    suspect: list[str] = []
    expected_false: list[str] = []
    for entry in audit_entries:
        if not isinstance(entry, dict):
            continue
        task_id = entry.get("task_id", "?")
        verified = entry.get("verified_via_command")
        if verified is True:
            if task_id in tool_call_task_ids:
                consistent.append(task_id)
            else:
                suspect.append(task_id)
        else:
            expected_false.append(task_id)
    return {
        "consistent": consistent,
        "suspect": suspect,
        "expected_false": expected_false,
    }


def main() -> int:
    repo_root = Path(__file__).resolve().parent.parent
    evals_dir = repo_root / ".harness" / "evals"
    out_path = repo_root / "tests" / "audit-report.md"

    eval_files = sorted(p for p in evals_dir.glob("sprint-*-r*.md"))

    report_lines: list[str] = [
        "# Adversarial Hygiene Audit Report",
        "",
        "Generated by `tests/generate-audit-report.py` on demand. Sprint 11 Criterion 12.",
        "",
        "Walks every Sprint 6–10 round eval (`sprint-NN-rR.md`), extracts the trailing",
        "`## Transcript Trailer` JSON block where present, and categorizes each",
        "`criteria_audit` entry under three buckets:",
        "",
        "- **Consistent (a):** `verified_via_command: true` AND a matching `task_id` in `tool_calls`.",
        "  The verifier did run.",
        "- **Suspect (b):** `verified_via_command: true` AND NO matching `task_id` in `tool_calls`.",
        "  The calibration-drift case Sprint 10's Adversarial Hygiene Rules 1–3 exist to flag.",
        "- **Expected-false (c):** `verified_via_command: false` — LLM-judge or prose-graded by design.",
        "",
        "Evals that predate Sprint 10's `criteria_audit` schema (Sprint 6/7/8/9) report",
        '"no per-criterion audit channel" rather than counting toward any bucket.',
        "",
        "## Per-Sprint Findings",
        "",
    ]

    grand_consistent = 0
    grand_suspect = 0
    grand_expected_false = 0
    grand_no_channel = 0
    suspect_details: list[str] = []

    for eval_path in eval_files:
        rel = eval_path.relative_to(repo_root).as_posix()
        text = eval_path.read_text(encoding="utf-8")
        trailer = find_trailer(text)
        if trailer is None:
            report_lines.append(f"### {rel}")
            report_lines.append("")
            report_lines.append("No `## Transcript Trailer` section or malformed JSON. Skipped.")
            report_lines.append("")
            continue

        audit_entries = trailer.get("criteria_audit")
        report_lines.append(f"### {rel}")
        report_lines.append("")

        if not audit_entries:
            report_lines.append(
                "No per-criterion audit channel (predates Sprint 10's `criteria_audit` schema)."
            )
            report_lines.append("")
            grand_no_channel += 1
            continue

        cats = categorize_audit(trailer)
        c_count = len(cats["consistent"])
        s_count = len(cats["suspect"])
        f_count = len(cats["expected_false"])
        report_lines.append(f"- Consistent (a): **{c_count}** — {', '.join(cats['consistent']) or '(none)'}")
        report_lines.append(f"- Suspect (b): **{s_count}** — {', '.join(cats['suspect']) or '(none)'}")
        report_lines.append(f"- Expected-false (c): **{f_count}** — {', '.join(cats['expected_false']) or '(none)'}")
        report_lines.append("")
        grand_consistent += c_count
        grand_suspect += s_count
        grand_expected_false += f_count
        for tid in cats["suspect"]:
            suspect_details.append(f"- {rel} → task_id={tid}")

    report_lines.append("## Summary")
    report_lines.append("")
    report_lines.append(f"- Eval files walked: **{len(eval_files)}**")
    report_lines.append(f"- Files with no `criteria_audit` channel: **{grand_no_channel}**")
    report_lines.append(f"- Consistent (a) entries across all evals: **{grand_consistent}**")
    report_lines.append(f"- Suspect (b) entries across all evals: **{grand_suspect}**")
    report_lines.append(f"- Expected-false (c) entries across all evals: **{grand_expected_false}**")
    report_lines.append("")
    if grand_suspect == 0:
        report_lines.append(
            "**No suspect entries found.** Every `verified_via_command: true` flag in the Sprint 6–10 "
            "evaluation history is backed by a matching `tool_calls` entry — the adversarial-hygiene "
            "discipline introduced by Sprint 10 holds across the full eval history available."
        )
    else:
        report_lines.append("**Suspect entries — investigate:**")
        report_lines.extend(suspect_details)

    report_lines.append("")
    report_lines.append("## Audit-Logic Verification")
    report_lines.append("")
    report_lines.append(
        "A reader of this report should be able to verify the audit logic is sound by inspecting "
        "any one suspect entry: open the cited eval file, locate the `## Transcript Trailer` block, "
        "find the `criteria_audit` entry with `verified_via_command: true`, and confirm there is no "
        "matching `task_id` in `tool_calls`. A `verified_via_command: false` entry with no tool call "
        "is fine (LLM-judge); a `true` entry with no tool call is the suspect case Sprint 10's "
        "Adversarial Hygiene Rules 1–3 exist to flag."
    )
    report_lines.append("")

    out_path.write_text("\n".join(report_lines), encoding="utf-8")
    print(f"PASS: wrote {out_path}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
